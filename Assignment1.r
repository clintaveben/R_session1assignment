2.	What is recycling  of elements in a Vector

Ans : Recycling occurs when vector arithmetic  is performed on multiple 
vectors of different size. R takes the shorter vector repeats it until 
it becomes long enough to match the longer one.


3.	Give an example of recycling of elements

Ans : suppose one vector a= c (1,2,3,4,5,6) and other b= c(1,3). 
When we add these two vectors  a+b, it will take the shorter vector 
and repeat them until it becomes long enough to match the longer one 
as c(1,3,1,3,1,3) and gives us the result as [1] 2,5,4,7,6,9

